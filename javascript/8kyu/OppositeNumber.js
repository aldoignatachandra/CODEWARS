// Opposite Number

/* TASK
Very simple, given a number, find its opposite.

Examples:
1: -1
14: -14
-34: 34
*/

// SOLUTION
function opposite(number) {
  return number * -1;
}

// TEST CASE
console.log("TEST 1 =>", opposite(17));
console.log("TEST 2 =>", opposite(-1));
console.log("TEST 3 =>", opposite(10));
